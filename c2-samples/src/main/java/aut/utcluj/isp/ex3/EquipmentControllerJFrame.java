    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if(this.jTextField2.getText().isBlank())
        {
            this.jLabel4.setText("ERROR: CAN'T LEAVE SERIAL NUMBER EMPTY");
            return;

        }

        if(this.jTextField1.getText().isBlank() && !this.jTextField3.getText().isBlank())
        {
            this.jLabel4.setText("ERROR: CAN'T HAVE OWNER WITHOUT ADDING EQUIPMENT NAME");
            return;
        }

        this.jLabel4.setText("");

        if(this.jTextField1.getText().isBlank())
        {
            Equipment equi = new Equipment( this.jTextField2.getText()  );
            eq.addEquipment(equi);

        }
        else{

            if(this.jTextField3.getText().isBlank())
            {
                Equipment equi = new Equipment( this.jTextField1.getText() , this.jTextField2.getText() );
                eq.addEquipment(equi);
            }
            else
            {
                Equipment equi = new Equipment( this.jTextField1.getText() , this.jTextField2.getText(), this.jTextField3.getText()  );
                eq.addEquipment(equi);
            }

        }

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][][][] {

            },
            new String [] {
                "Name","Serial Number", "Owner", "Taken"
            }
        ));

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for(int i = 0;i < eq.getNumberOfEquipments();i++ )
        {
            try {

                model.addRow( new Object[]{ eq.equipmentList.get(i).getName(),
                    eq.equipmentList.get(i).getSerialNumber(),eq.equipmentList.get(i).getOwner(),
                    eq.equipmentList.get(i).isTaken()});

        } catch (Exception ex) {

        }
        }

        this.jLabel5.setText("Nr of equipment : " + eq.getNumberOfEquipments());

    }//GEN-LAST:event_jButton1ActionPerformed


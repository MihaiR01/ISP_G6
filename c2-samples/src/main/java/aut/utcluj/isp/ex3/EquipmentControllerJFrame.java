    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][][][] {

            },
            new String [] {
                "Name","Serial Number", "Owner", "Taken"
            }
        ));

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Map<String,List<Equipment>> mp = eq.getEquipmentsGroupedByOwner();

        for( String i : mp.keySet() )
        {
            for(int j = 0;j < mp.get(i).size();j++ )
            try {

                model.addRow( new Object[]{
                    mp.get(i).get(j).getName(),
                    mp.get(i).get(j).getSerialNumber(),
                    mp.get(i).get(j).getOwner(),
                    mp.get(i).get(j).isTaken()});

        } catch (Exception ex) {

        }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

